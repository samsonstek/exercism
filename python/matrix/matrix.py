class Matrix:
    def __init__(self, matrix_string):
        self.matrix = [[int(value) for value in element.split(" ")] for element in matrix_string.split("\n")]

        
    def row(self, index):
        return (self.matrix[index-1])


    def column(self, index):
        return [int(element[index-1]) for element in self.matrix]

















































































































































